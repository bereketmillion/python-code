username_label=tk.Label(frame,text="User Name",bg="#008080",fg="#ffffff",font=("arial",14),pady=2)
password_label=tk.Label(frame,text="Password",bg="#008080",fg="#ffffff",font=("arial",14),pady=2)
firstname_entry=tk.Entry(frame,font=("arial",14))
