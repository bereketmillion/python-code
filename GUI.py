import sqlite3
con=sqlite3.connect("it.db")
cursor=con.cursor()
cursor.execute("create table student(firstname text NOT NULL,secondname text NOT NULL, id text primary key NOT NULL,department text NOT NULL,year  integer NOT NULL,username text NOT NULL,password text NOT NULL)")
con.commit()
con.close()
import tkinter as tk
top=tk.Tk()
top.title("python")
top.geometry("400x600")
top.configure(bg="#008080")
frame=tk.Frame(bg="#008080")
registration_label=tk.Label(frame,text="Registration",bg="#008080",fg="#ffffff",font=("arial",30),pady=10)
firstname_label=tk.Label(frame,text="First Name",bg="#008080",fg="#ffffff",font=("arial",14),pady=2)
